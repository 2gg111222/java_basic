package 메서드연습;

public class 계산기3 {
	public int add(int x, int y) {//더하기ㅣ2-3]// 파라메터 매개변수
		//전달된 값을 저장하는 매개 역할을 수행, 중매 매 낄 개
		return x + y;
		
	}
		
	public int m(int x, int y) {//매개변수(parameter)
		
		return  x - y;
		
	}
	public int a(int x, int i) {//매개변수(parameter)
		return x*i;
		
	}
	public int b(int y, int j) {//매개변수(parameter)
		return y*j;
	}
	public int c(int xi, int yj) {//매개변수(parameter)
		return xi + yj;
	}
	public int d(int xyi, int xy) {//매개변수(parameter)
		return xyi / xy;
	}
	
}